# Arithmetic Operators
#? Arithmetic operators are used with numeric values to perform common mathematical operations:

"""
? (+)       Addition            x + y  

? (-)       Substraction        x - y

? (*)       Multiplication      x * y

? (/)       Division            x / y

? (%)       Modulus             x % y

? (**)      Exponentiation      x ** y squre

? (//)    Floor division        x // y

"""

#? Here is an example using different arithmetic operators:

a = 5
b = 3

print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a % b)
print(a ** b)
print(a // b)
#? / division(Returns a float)
#? // Floor division(Always returns an int)
print(12/5) #? Returns a float
print(12//5) #? Returns an int