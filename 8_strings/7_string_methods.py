#String Methods
#? Python has a set of built-in methods that you can use on strings.

# capitalize() #? Converts the first character to upper case
# casefold() #? Converts all characters to lower-case(More aggressive than lower())
# center() #? Returns a center string
# count() #? Returns the number of times a specified value appears in a string
# encode() #? Returns an encoded version of the string
# endswith() #? Returns true if the string ends with the specified value
# startswith() #? Returns true if the string starts with the specified value
# find() #? Searches the string for a specified value and returns the position of where it was found
# rfind() #? Searches the string for a specified value and returns the last position of where it was found
# format() #? Formats specified values in a string
# format_map() #? Formats specified values in a string
# index() #? Searches the string for a specified value and returns the position of where it was found
# rindex() #? Searches the string for a specified value and returns the last position of where it was found
# isalnum() #? Returns True if all characters in the string are alphanumeric
# isalpha() #? Returns True if all characters in the string are in the alphabet
# isdecimal() #? Returns True if all characters in the string are decimals
# isdigit() #? Returns True if all characters in the string are digits
# islower() #? Returns True if all characters in the string are lower case
# isnumeric() #? Returns True if all characters in the string are numeric
# isprintable() #? Returns True if all characters in the string are printable
# isspace() #? Returns True if all characters in the string are whitespaces
# istitle() #? Returns True if the string follows the rules of a title
# isupper() #? Returns True if all characters in the string are upper case
# join() #? Joins the elements of an iterable to the end of the string
# lower() #? Converts a string into lower case
# lstrip() #? Removes whitespace of left-side
# rstrip() #? Removes whitespace of right-side
# strip() #? Removes whitspace of both side (left and right)
# replace() #? modifies the string with the specified value
# split() #? Splits the string at the specified separator, and returns a list
# splitlines() #? Splits the string at line breaks and returns a list
# title() #? Converts the first character of each word to upper case
# upper() #? Converts a string into upper case



