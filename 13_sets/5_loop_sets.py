# Loop Sets
#? Loop Items
#* You can loop through the set items by using a for loop:
set1 = {"January", "February", "March", "April", "May", "Jun", "July", "August", "September", "November", "December"}
for i in set1: #* Through items
    print(i)

#! We cannot access set items using while loop. Set object is not subscriptable and it's unordered/unindexed


